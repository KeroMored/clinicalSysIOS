  static final List<String> _tips = _buildTips();

  static List<String> _buildTips() {
    final result = <String>[];
    final seen = <String>{};

    void addTip(String value) {
      final normalized = value.trim();
      if (normalized.isEmpty || seen.contains(normalized)) return;
      seen.add(normalized);
      result.add(normalized);
    }

    for (final seed in _seedTips) {
      addTip(seed);
    }

    int i = 0;
    while (result.length < tipsCount && i < 5000) {
      final behavior = _behaviors[i % _behaviors.length];
      final benefit = _benefits[(i * 3 + 7) % _benefits.length];
      final quickAction = _quickActions[(i * 5 + 11) % _quickActions.length];

      switch (i % 6) {
        case 0:
          addTip(
            'نصيحة عملية: $behavior عادة بسيطة لكنها فعالة لأنها $benefit. خطوة اليوم: $quickAction.',
          );
          break;
        case 1:
          addTip(
            'حقيقة صحية: $behavior يرتبط علميا بأنه $benefit. تطبيق سريع: $quickAction.',
          );
          break;
        case 2:
          addTip(
            'تذكير طبي يومي: لا تستهين بـ $behavior، فهو $benefit. جرّب الآن: $quickAction.',
          );
          break;
        case 3:
          addTip(
            'لو هدفك صحة أفضل، ابدأ بـ $behavior؛ النتيجة المتوقعة أنه $benefit. تنفيذ فوري: $quickAction.',
          );
          break;
        case 4:
          addTip(
            'من أكثر العادات التي يحبها الأطباء: $behavior، لأنها $benefit. مهمة اليوم: $quickAction.',
          );
          break;
        default:
          addTip(
            'قرار صحي ذكي: $behavior. الفائدة الأهم: $benefit. الخطة المختصرة: $quickAction.',
          );
          break;
      }

      i++;
    }

    // Fallback safety to guarantee exact count even if future edits reduce variety.
    int safety = 1;
    while (result.length < tipsCount) {
      addTip(
        'رسالة صحية خاصة رقم $safety: استمر على عادة واحدة صحيحة يوميا، فالاستمرارية أقوى من المثالية.',
      );
      safety++;
    }

    return result.take(tipsCount).toList(growable: false);
  }

  static List<String> get allTips => _tips;

  static int dayOfYear(DateTime date) {
    final start = DateTime(date.year, 1, 1);
    return date.difference(start).inDays + 1;
  }

  static int tipIndexForDate(DateTime date) {
    final day = dayOfYear(date);
    return (day - 1) % tipsCount;
  }

  static String getTipForDate(DateTime date) {
    return _tips[tipIndexForDate(date)];
  }
